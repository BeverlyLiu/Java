package Basics;

public class LoopsConcept {

	public static void main(String[] args) {
		
		//while loop
		int i = 1; //initialization
		while(i<=10) { //conditional
			System.out.println(i);
			i++; //incremental/decremental
		}
		System.out.println();
		//for loop
		for(i=10; i>=1; i--) { //init, condition, incremental
			System.out.println(i);
		}
	}

}