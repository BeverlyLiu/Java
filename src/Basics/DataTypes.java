package Basics;

public class DataTypes {

	//main() method -- starting execution point of the program
	public static void main(String[] args) {
		
		//primitive data type: int, double, char, boolean
		//int
		int i = 10;
		i = 0;
		int j = -1;
		System.out.println(i+j);
		
		// double
		double d = 12.33;
		double d1 = -100; //-100.00
		System.out.println(d - d1);
		
		// char: only single digit value, should be written within single quotes
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '$';
		
		// boolean 
		boolean b1 = true;
		boolean	b2 = false;
		
		//String: is a class, not a data type
		String s = "Hello World";
		String s1 = "Selenium";
		String s2 = "1000";
		String s3 = "12.33";
		
		System.out.println(s1);
		
		

	}

}
